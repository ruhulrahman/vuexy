    //check username function return the email
    public function checkUsername(Request $request)
    {
        $model = User::where('email', $request->email)->where('is_active', 1)->first();

        if (!$model) {
            return ['success' => false, 'message' => 'username not found.'];
        }

        return [
            'success' => true,
            'data' => $model
        ];
    }

